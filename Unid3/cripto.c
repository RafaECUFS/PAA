uint8_t* String_Hex_Convert(char* chave) {
    uint8_t tam = strlen(chave);
    uint8_t* aes_chave = (uint8_t*)malloc(sizeof(uint8_t) * (tam / 2));
    if (aes_chave == NULL) {
        return NULL; // Se a alocação falhar, retorna NULL
    }

    char hexPart[3] = {'\0'}; // Parte hexadecimal com 2 caracteres (mais o terminador nulo)

    // Iterar sobre a string em incrementos de 2 caracteres
    for (int i = 0; i < tam; i += 2) {
        // Copiar 2 caracteres da string para hexPart
        hexPart[0] = chave[i];
        hexPart[1] = chave[i + 1];

        // Converter a parte hexadecimal para um valor inteiro
        aes_chave[i / 2] = strtol(hexPart, NULL, 16);
    }
    return aes_chave;
}

uint8_t power_mod(uint8_t base, uint8_t exp, uint8_t modulus) {
    if (exp == 0)
        return 1;
    else if (exp % 2)
        return (base * power_mod(base, exp - 1, modulus)) % modulus;
    else {
        uint8_t temp = power_mod(base, exp / 2, modulus);
        return (temp * temp) % modulus;
    }
}


